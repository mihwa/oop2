			public class Gugudan {
				public static void main(String[] args) {
					for (int i = 2; i < 10; i++) {  //변수i값이 10보다 작을때
						for (int j = 1; j < 10; j++) {//변수j값이 10보다 작을때 for문을 빠져나가는 것이다
							System.out.print(i+"*"+j+"="+(i*j)+"\n");
						}
						System.out.println();
						
					}
					System.out.println();

			}
			
	}
		
		

	
