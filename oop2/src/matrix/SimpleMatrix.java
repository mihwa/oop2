package matrix;

	public class SimpleMatrix {
		public static void main(String[] args) {
				int k=1;	
				
			for (int i = 0; i < 3; i++) {  //행(가로)
				for (int j = 0; j < 5; j++) {  //열(세로)
					System.out.print(k+"\t");//
					k++;
				}
				System.out.println();
				
			}
			
			}
			
		}
	
